using System;

namespace OdeMod.CardMode.Scenes.ConfigScene.ConfigSystem.Attributes
{
    [AttributeUsage(AttributeTargets.Field | AttributeTargets.Property, AllowMultiple = false)]
    internal class ConfigSbyteRangeAttribute : Attribute
    {
        public readonly sbyte Min;
        public readonly sbyte Max;
        public ConfigSbyteRangeAttribute(sbyte min, sbyte max)
        {
            if (max < min)
                throw new Exception($"The max value:{max} is less than the min value:{min}");
            Min = min;
            Max = max;
        }
    }
}